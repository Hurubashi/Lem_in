#include "libft.h"

char		*ft_strdup(const char *s1)
{
	char	*str;
	int		i;

	i = 0;
	while (s1[i])
	{
		i++;
	}
	if ((str = (char*)malloc(i + 1)) == NULL)
	{
		return (0);
	}
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
