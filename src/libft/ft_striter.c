#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s) - 1;
		while (i >= 0)
		{
			f(&s[i]);
			i--;
		}
	}
}
