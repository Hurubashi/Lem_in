#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;

	i = 0;
	if (s != NULL && f != NULL)
	{
		i = ft_strlen(s) - 1;
		while (i >= 0)
		{
			f(i, &s[i]);
			i--;
		}
	}
}
